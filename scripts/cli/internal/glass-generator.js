  opacity?: number;
  
  /**
   * Border opacity (0-1)
   * @default 0.3
   */
  borderOpacity?: number;
  
  /**
   * Glass preset name ('subtle' | 'default' | 'strong' | 'frost' | 'crystal')
   */
  preset?: 'subtle' | 'default' | 'strong' | 'frost' | 'crystal';
